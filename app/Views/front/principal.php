          tuki tukiiii Durante siglos la posesión y el cuidado de los bonsáis estuvo ligado a los nobles y a las personas de la alta sociedad. Según la tradición, aquellos que podían conservar un árbol en maceta tenían asegurada la eternidad. Así fue como los monjes disponían los árboles pequeños en vasijas a lo largo de las escaleras de los templos y hasta eran fuente de culto.            
